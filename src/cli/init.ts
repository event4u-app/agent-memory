// Bootstrap a consumer project so it can run the agent-memory sidecar
// without hand-writing compose/env/gitignore plumbing. Idempotent: running
// it twice is always safe (existing files are preserved unless --force).
//
// Target files (all inside the caller's current working directory):
//   - docker-compose.agent-memory.yml  — standalone compose (Pattern A)
//   - .env.agent-memory                — environment overrides (optional)
//   - .gitignore                       — marker-delimited section appended
//
// The command is pure I/O plus the embedded templates; no DB access, no
// network, no side-effects outside `cwd`. A1 · runtime-trust.

import { access, constants, readFile, writeFile } from "node:fs/promises";
import { join } from "node:path";

export const GITIGNORE_MARKER_START = "# >>> agent-memory (managed by `memory init`)";
export const GITIGNORE_MARKER_END = "# <<< agent-memory";
export const GITIGNORE_PATTERNS = [".env.agent-memory"];

