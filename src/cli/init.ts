export type InitFileStatus = "created" | "skipped" | "updated";

export interface InitFileResult {
	path: string;
	status: InitFileStatus;
	reason?: string;
}

export interface InitReport {
	status: "ok";
	cwd: string;
	files: InitFileResult[];
}

export interface InitOptions {
	cwd?: string;
	force?: boolean;
}

async function exists(path: string): Promise<boolean> {
	try {
		await access(path, constants.F_OK);
		return true;
	} catch {
		return false;
	}
}

async function writeIfMissing(
	path: string,
	contents: string,
	force: boolean,
): Promise<InitFileResult> {
	if ((await exists(path)) && !force) {
		return { path, status: "skipped", reason: "already exists" };
	}
	await writeFile(path, contents, "utf8");
	return { path, status: "created" };
}

export function applyGitignoreBlock(existing: string): { content: string; changed: boolean } {
	const block = [GITIGNORE_MARKER_START, ...GITIGNORE_PATTERNS, GITIGNORE_MARKER_END, ""].join(
		"\n",
	);
	if (existing.includes(GITIGNORE_MARKER_START)) {
		return { content: existing, changed: false };
	}
	const prefix = existing.length === 0 || existing.endsWith("\n") ? existing : `${existing}\n`;
	return { content: `${prefix}${block}`, changed: true };
}

async function applyGitignore(path: string): Promise<InitFileResult> {
	const existed = await exists(path);
	const previous = existed ? await readFile(path, "utf8") : "";
	const { content, changed } = applyGitignoreBlock(previous);
	if (!changed) {
		return { path, status: "skipped", reason: "marker already present" };
	}
	await writeFile(path, content, "utf8");
	return existed
		? { path, status: "updated", reason: "marker appended" }
		: { path, status: "created" };
}

export async function runInit(options: InitOptions = {}): Promise<InitReport> {
	const cwd = options.cwd ?? process.cwd();
	const force = options.force ?? false;
	const files: InitFileResult[] = [];
	files.push(
		await writeIfMissing(
			join(cwd, "docker-compose.agent-memory.yml"),
			DOCKER_COMPOSE_TEMPLATE,
			force,
		),
	);
	files.push(await writeIfMissing(join(cwd, ".env.agent-memory"), ENV_TEMPLATE, force));
	files.push(await applyGitignore(join(cwd, ".gitignore")));
	return { status: "ok", cwd, files };
}

export function renderInitSummary(report: InitReport): string {
	const lines = [`agent-memory init → ${report.cwd}`];
	for (const f of report.files) {
		const marker = f.status === "created" ? "+" : f.status === "updated" ? "~" : "=";
		const suffix = f.reason ? ` (${f.reason})` : "";
		lines.push(`  ${marker} ${f.path}${suffix}`);
	}
	return lines.join("\n");
}
