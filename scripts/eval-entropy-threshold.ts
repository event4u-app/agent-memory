/**
 * Calibration harness for the `HIGH_ENTROPY_DETECTED` heuristic.
 *
 * Reads two corpora:
 *   - tests/fixtures/entropy-corpus/non-secrets.txt  (must-pass)
 *   - tests/fixtures/entropy-corpus/secrets.txt      (must-catch)
 *
 * For every candidate threshold in `THRESHOLDS`, it runs each line through
 * the same logic as the production guard (`scanForSecrets`) with the
 * threshold overridden, records {tp, fp, tn, fn}, and prints a
 * precision/recall/F1 matrix. The script also writes a deterministic
 * markdown block into `docs/security/entropy-calibration.md` between the
 * GEN markers, so CI can drift-guard the choice the same way it does for
 * the pattern catalog.
 *
 * This is the single source of the "the threshold is reasoned, not
 * guessed" claim in `agents/roadmaps/secret-safety.md` (II2 Done).
 *
 * Run with: `npm run entropy:eval`.
 */

import { readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";
import { scanForSecrets } from "../src/security/secret-guard.js";

const THRESHOLDS = [3.5, 4.0, 4.5, 5.0];
const MIN_LENGTH = 20;

const MARKER_START = "<!-- GEN:ENTROPY-CALIBRATION:START -->";
const MARKER_END = "<!-- GEN:ENTROPY-CALIBRATION:END -->";
const DOC_PATH = resolve(process.cwd(), "docs/security/entropy-calibration.md");
const CORPUS_NON = resolve(process.cwd(), "tests/fixtures/entropy-corpus/non-secrets.txt");
const CORPUS_SEC = resolve(process.cwd(), "tests/fixtures/entropy-corpus/secrets.txt");

interface Metrics {
	tp: number;
	fp: number;
	tn: number;
	fn: number;
	precision: number;
	recall: number;
	f1: number;
}

function readCorpus(path: string): string[] {
	return readFileSync(path, "utf8")
		.split(/\r?\n/)
		.map((l) => l.trim())
		.filter((l) => l.length > 0 && !l.startsWith("#"));
}

/**
 * Returns true iff the production guard would flag `line` as high-entropy
 * at `threshold`. We feed the line through `scanForSecrets` wrapped in
 * quotes (the guard's regex requires quoting) and check for the
 * `high_entropy` pattern. Lines that match a named catalog pattern are
 * excluded — this harness only measures the residual heuristic, not the
 * catalog.
 */
function isFlagged(line: string, threshold: number): boolean {
	const detections = scanForSecrets(`"${line}"`, undefined, { threshold, minLength: MIN_LENGTH });
	const hasNamed = detections.some((d) => d.pattern !== "high_entropy");
	if (hasNamed) return false;
	return detections.some((d) => d.pattern === "high_entropy");
}

function evaluate(nonSecrets: string[], secrets: string[], threshold: number): Metrics {
	let tp = 0;
	let fn = 0;
	let fp = 0;
	let tn = 0;
	for (const line of secrets) (isFlagged(line, threshold) ? tp++ : fn++);
	for (const line of nonSecrets) (isFlagged(line, threshold) ? fp++ : tn++);
	const precision = tp + fp === 0 ? 0 : tp / (tp + fp);
	const recall = tp + fn === 0 ? 0 : tp / (tp + fn);
	const f1 = precision + recall === 0 ? 0 : (2 * precision * recall) / (precision + recall);
	return { tp, fp, tn, fn, precision, recall, f1 };
}

function fmt(n: number): string {
	return (Math.round(n * 1000) / 1000).toFixed(3);
}

function renderMatrix(results: { threshold: number; m: Metrics }[]): string {
	const rows = results
		.map(
			({ threshold, m }) =>
				`| ${threshold.toFixed(1)} | ${m.tp} | ${m.fn} | ${m.fp} | ${m.tn} | ${fmt(m.precision)} | ${fmt(m.recall)} | ${fmt(m.f1)} |`,
		)
		.join("\n");
	return [
		"| Threshold | TP | FN | FP | TN | Precision | Recall | F1 |",
		"|-----------|----|----|----|----|-----------|--------|----|",
		rows,
	].join("\n");
}

function chooseThreshold(results: { threshold: number; m: Metrics }[]): number {
	// Pick the F1-maximizing threshold; break ties by preferring lower
	// false-positive count (operators pay for every FP).
	let best = results[0];
	if (!best) throw new Error("no thresholds evaluated");
	for (const r of results) {
		if (
			r.m.f1 > best.m.f1 ||
			(Math.abs(r.m.f1 - best.m.f1) < 1e-9 && r.m.fp < best.m.fp)
		) {
			best = r;
		}
	}
	return best.threshold;
}

function render(results: { threshold: number; m: Metrics }[], chosen: number): string {
	return [
		MARKER_START,
		"",
		`<!-- auto-generated by scripts/eval-entropy-threshold.ts — do not edit by hand -->`,
		"",
		`**Chosen threshold:** \`${chosen.toFixed(1)}\` bits/char (min length ${MIN_LENGTH}).`,
		"",
		renderMatrix(results),
		"",
		MARKER_END,
	].join("\n");
}

function writeDoc(block: string): void {
	const current = (() => {
		try {
			return readFileSync(DOC_PATH, "utf8");
		} catch {
			return "";
		}
	})();
	const startIdx = current.indexOf(MARKER_START);
	const endIdx = current.indexOf(MARKER_END);
	if (startIdx !== -1 && endIdx !== -1 && endIdx > startIdx) {
		const before = current.slice(0, startIdx);
		const after = current.slice(endIdx + MARKER_END.length);
		writeFileSync(DOC_PATH, `${before}${block}${after}`);
	} else {
		writeFileSync(DOC_PATH, block);
	}
}

function main(): void {
	const nonSecrets = readCorpus(CORPUS_NON);
	const secrets = readCorpus(CORPUS_SEC);
	const results = THRESHOLDS.map((t) => ({
		threshold: t,
		m: evaluate(nonSecrets, secrets, t),
	}));
	const chosen = chooseThreshold(results);

	console.log(`non-secret samples: ${nonSecrets.length}`);
	console.log(`secret samples:     ${secrets.length}`);
	console.log("");
	console.log(renderMatrix(results));
	console.log("");
	console.log(`Chosen (max-F1, min-FP tiebreak): ${chosen.toFixed(1)}`);

	writeDoc(render(results, chosen));
}

main();
